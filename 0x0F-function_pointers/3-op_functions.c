#include "3-calc.h"
/**
 * op_add - adds a and b.
 *
 * @a: int.
 * @b: int.
 *
 * Return: the sum.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_mul - multiplies a with b.
 *
 * @a: int.
 * @b: int.
 *
 * Return: product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_sub - get diff of a with b
 *
 * @a: int.
 * @b: int.
 *
 * Return: diff of a with b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mod - gets modulu of a with b.
 *
 * @a: int.
 * @b: int.
 *
 * Return: modules of a with b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
/**
 * op_div - divide a with b.
 *
 * @a: int
 * @b: int
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
